def _write_overlay(ref_rgb: np.ndarray, render_rgb: np.ndarray,
                   path: Path) -> None:
    h, w = ref_rgb.shape[:2]
    render_rgb = cv2.resize(render_rgb, (w, h))
    blend = cv2.addWeighted(ref_rgb, 0.5, render_rgb, 0.5, 0.0)
    top = np.hstack([ref_rgb, render_rgb])
    label_bar = np.zeros((28, top.shape[1], 3), np.uint8)
    cv2.putText(label_bar, "reference", (10, 20), cv2.FONT_HERSHEY_SIMPLEX,
                0.6, (255, 255, 255), 1)
    cv2.putText(label_bar, "render", (w + 10, 20), cv2.FONT_HERSHEY_SIMPLEX,
                0.6, (255, 255, 255), 1)
    out = np.vstack([label_bar, top, np.hstack([blend, blend])])
    cv2.imwrite(str(path), out)


def _write_silhouette_comparison(ref: np.ndarray, render: np.ndarray,
                                 path: Path) -> None:
    h, w = ref.shape
    out = np.zeros((h, w, 3), np.uint8)
    both = np.logical_and(ref, render)
    ref_only = np.logical_and(ref, 1 - render)
    render_only = np.logical_and(render, 1 - ref)
    out[both > 0] = (180, 180, 180)
    out[ref_only > 0] = (0, 255, 0)       # green: missing in render
    out[render_only > 0] = (255, 0, 255)  # magenta: extra in render
    cv2.imwrite(str(path), out)


def _write_depth_comparison(ref_depth: Optional[np.ndarray],
                            render_depth: np.ndarray, mask: np.ndarray,
                            path: Path) -> None:
    h, w = render_depth.shape
    rd = render_depth.astype(np.float32)
    if rd.max() > rd.min():
        rd = (rd - rd.min()) / (rd.max() - rd.min())
    rd = np.where(mask > 0, rd, 0.0)
    right = cv2.applyColorMap((rd * 255).astype(np.uint8), cv2.COLORMAP_VIRIDIS)
    if ref_depth is None:
        # honest proxy: distance transform of the reference mask
        proxy = cv2.distanceTransform(mask.astype(np.uint8), cv2.DIST_L2, 5)
        if proxy.max() > 0:
            proxy = proxy / proxy.max()
        left = cv2.applyColorMap((proxy * 255).astype(np.uint8),
                                 cv2.COLORMAP_VIRIDIS)
    else:
        a = ref_depth.astype(np.float32)
        a = cv2.resize(a, (w, h), interpolation=cv2.INTER_NEAREST)
        if a.max() > a.min():
            a = (a - a.min()) / (a.max() - a.min())
        a = np.where(mask > 0, a, 0.0)
        left = cv2.applyColorMap((a * 255).astype(np.uint8),
                                 cv2.COLORMAP_VIRIDIS)
    cv2.imwrite(str(path), np.hstack([left, right]))


def _encode_turntable(frames_dir: Path, out_path: Path) -> bool:
    frames = sorted(frames_dir.glob("tt_*.png"))
    if not frames:
        return False
    first = cv2.imread(str(frames[0]))
    if first is None:
        return False
    h, w = first.shape[:2]
    w, h = w - (w % 2), h - (h % 2)   # mp4 needs even dims
    vw = cv2.VideoWriter(str(out_path), cv2.VideoWriter_fourcc(*"mp4v"),
                         12.0, (w, h))
    if not vw.isOpened():
        return False
    for f in frames:
        img = cv2.imread(str(f))
        if img is None:
            continue
        vw.write(cv2.resize(img, (w, h)))
    vw.release()
    return out_path.exists() and out_path.stat().st_size > 0


