def validate_reconstruction(manifest: BlenderManifest, plan: ConstructionPlan,
                            seg: SegmentationResult, crop_meta: CropMetadata,
                            project_dir: str, cfg: PipelineConfig
                            ) -> ValidationResult:
    project = Path(project_dir).resolve()
    val_dir = project / "validation"
    val_dir.mkdir(parents=True, exist_ok=True)
    result = ValidationResult()

    if not manifest.success:
        result.passed = False
        result.notes.append("blender build failed; skipping render validation")
        result.notes.extend(manifest.errors[:3])
        return result

    # ---- reference data ------------------------------------------------
    mask_img = cv2.imread(seg.mask_path, cv2.IMREAD_GRAYSCALE)
    if mask_img is None:
        raise FileNotFoundError("segmentation mask not found: %s" % seg.mask_path)
    ref_mask = _binarize(remap_to_crop(mask_img, crop_meta))
    rgba = cv2.imread(seg.rgba_path, cv2.IMREAD_COLOR)
    if rgba is not None:
        ref_rgb = remap_to_crop(rgba, crop_meta, cv2.INTER_LINEAR)
    else:
        ref_rgb = cv2.cvtColor(ref_mask * 255, cv2.COLOR_GRAY2BGR)

    # ---- render passes via Blender --------------------------------------
    script_path = generate_render_script(plan, ref_mask, str(project), cfg)
    render_manifest = runner.run_blender(script_path, str(project), cfg)
    if not render_manifest.success:
        result.passed = False
        result.notes.append("render script failed")
        result.notes.extend(render_manifest.errors[:2])
        return result

    def load(name, flags=cv2.IMREAD_COLOR):
        img = cv2.imread(str(val_dir / name), flags)
        if img is None:
            raise FileNotFoundError("expected render missing: %s" % name)
        return img

    render_sil = _binarize(load("render_silhouette.png", cv2.IMREAD_GRAYSCALE))
    clay = load("render_clay.png")
    shaded = load("render_shaded.png")
    depth_img = load("render_depth.png", cv2.IMREAD_UNCHANGED)
    if depth_img.ndim == 3:
        depth_img = cv2.cvtColor(depth_img, cv2.COLOR_BGR2GRAY)
    depth_f = depth_img.astype(np.float32) / (65535.0 if depth_img.dtype ==
                                              np.uint16 else 255.0)

    if render_sil.shape != ref_mask.shape:
        render_sil = cv2.resize(render_sil, (ref_mask.shape[1],
                                             ref_mask.shape[0]),
                                interpolation=cv2.INTER_NEAREST)
    shaded = cv2.resize(shaded, (ref_mask.shape[1], ref_mask.shape[0]))
    clay_gray = cv2.cvtColor(clay, cv2.COLOR_BGR2GRAY)
    clay_mask = _binarize(cv2.resize(clay_gray, (ref_mask.shape[1],
                                                 ref_mask.shape[0])), 20)

    union_mask = np.logical_or(ref_mask, render_sil).astype(np.uint8)

    # ---- metrics --------------------------------------------------------
    metrics = ValidationMetrics()
    metrics.silhouette_iou = _iou(ref_mask, render_sil)
    metrics.clay_silhouette_iou = _iou(ref_mask, clay_mask)
    metrics.contour_chamfer_distance = _chamfer(ref_mask, render_sil)
    metrics.feature_alignment_error_px = _feature_alignment(
        ref_rgb, shaded, union_mask)
    metrics.color_region_agreement = _color_agreement(
        ref_rgb, shaded, np.logical_and(ref_mask, render_sil).astype(np.uint8))
    metrics.perceptual_similarity = _perceptual_similarity(
        ref_rgb, shaded, union_mask)

    depth_path = project / "geometry" / "depth.png"
    ref_depth = None
    if depth_path.exists():
        rd = cv2.imread(str(depth_path), cv2.IMREAD_UNCHANGED)
        if rd is not None:
            if rd.ndim == 3:
                rd = cv2.cvtColor(rd, cv2.COLOR_BGR2GRAY)
            ref_depth = rd.astype(np.float32)
            if ref_depth.max() > 0:
                ref_depth = ref_depth / ref_depth.max()
    depth_resized = cv2.resize(depth_f, (ref_mask.shape[1], ref_mask.shape[0]))
    metrics.depth_correlation = _depth_correlation(
        ref_depth, depth_resized,
        np.logical_and(ref_mask, render_sil).astype(np.uint8))

    # ---- artifacts ------------------------------------------------------
    overlay_path = val_dir / "reference_overlay.png"
    _write_overlay(ref_rgb, shaded, overlay_path)
    result.overlay_path = str(overlay_path)

    sil_path = val_dir / "silhouette_comparison.png"
    _write_silhouette_comparison(ref_mask, render_sil, sil_path)
    result.silhouette_comparison_path = str(sil_path)

    depth_cmp_path = val_dir / "depth_comparison.png"
    _write_depth_comparison(ref_depth, depth_resized, ref_mask, depth_cmp_path)
    result.depth_comparison_path = str(depth_cmp_path)

    tt_path = val_dir / "turntable.mp4"
    if _encode_turntable(val_dir / "turntable_frames", tt_path):
        result.turntable_path = str(tt_path)
    else:
        result.notes.append("turntable.mp4 could not be encoded")

    SchemaIO.save_json(metrics, val_dir / "metrics.json")

    result.metrics = metrics
    target = cfg.refinement.target_silhouette_iou
    result.passed = (metrics.silhouette_iou or 0.0) >= target
    if not result.passed:
        result.notes.append(
            "silhouette_iou %.3f below target %.3f"
            % (metrics.silhouette_iou or 0.0, target))
    return result
