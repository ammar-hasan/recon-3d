def _binarize(img: np.ndarray, thresh: int = 127) -> np.ndarray:
    if img.ndim == 3:
        img = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
    return (img > thresh).astype(np.uint8)


def _iou(a: np.ndarray, b: np.ndarray) -> float:
    inter = float(np.logical_and(a, b).sum())
    union = float(np.logical_or(a, b).sum())
    return inter / union if union > 0 else 0.0


def _chamfer(a: np.ndarray, b: np.ndarray) -> float:
    """Symmetric chamfer distance between mask contours, normalised by the
    reference mask diagonal."""
    def contour_dist(src, dst):
        dt = cv2.distanceTransform((1 - dst).astype(np.uint8),
                                   cv2.DIST_L2, 3)
        edges = cv2.Canny((src * 255).astype(np.uint8), 50, 150) > 0
        vals = dt[edges]
        return float(vals.mean()) if vals.size else 0.0
    diag = _mask_diagonal(a)
    if diag <= 0:
        return 0.0
    return (contour_dist(a, b) + contour_dist(b, a)) / 2.0 / diag


def _mask_diagonal(mask: np.ndarray) -> float:
    ys, xs = np.nonzero(mask)
    if len(xs) == 0:
        return 0.0
    w = xs.max() - xs.min() + 1
    h = ys.max() - ys.min() + 1
    return float(math.hypot(w, h))


def _feature_alignment(ref_rgb: np.ndarray, render_rgb: np.ndarray,
                       mask: np.ndarray) -> Optional[float]:
    """Mean symmetric nearest-neighbour distance between internal edge
    points (Canny edges restricted to the mask interior), in pixels."""
    kernel = np.ones((5, 5), np.uint8)
    inner = cv2.erode(mask, kernel, iterations=2)

    def edges(img):
        gray = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY) if img.ndim == 3 else img
        e = cv2.Canny(gray, 60, 160) > 0
        return np.logical_and(e, inner > 0)

    ea, eb = edges(ref_rgb), edges(render_rgb)
    if ea.sum() < 10 or eb.sum() < 10:
        return None

    def mean_nn(src, dst):
        dt = cv2.distanceTransform((~dst).astype(np.uint8), cv2.DIST_L2, 3)
        return float(dt[src].mean())
    return (mean_nn(ea, eb) + mean_nn(eb, ea)) / 2.0


def _color_agreement(ref_rgb: np.ndarray, render_rgb: np.ndarray,
                     mask: np.ndarray) -> Optional[float]:
    m = mask > 0
    if m.sum() < 10:
        return None
    diff = np.abs(ref_rgb.astype(np.float32) -
                  render_rgb.astype(np.float32)).mean(axis=2)
    return float(1.0 - diff[m].mean() / 255.0)


def _perceptual_similarity(ref_rgb: np.ndarray, render_rgb: np.ndarray,
                           mask: np.ndarray) -> Optional[float]:
    a = cv2.cvtColor(ref_rgb, cv2.COLOR_BGR2GRAY).astype(np.float64) / 255.0
    b = cv2.cvtColor(render_rgb, cv2.COLOR_BGR2GRAY).astype(np.float64) / 255.0
    m = mask > 0
    a = np.where(m, a, 0.0)
    b = np.where(m, b, 0.0)
    try:
        from skimage.metrics import structural_similarity as ssim
        return float(ssim(a, b, data_range=1.0))
    except Exception:
        pass
    # fallback: masked normalised correlation
    av, bv = a[m], b[m]
    if av.size < 10 or av.std() < 1e-9 or bv.std() < 1e-9:
        return None
    r = float(np.corrcoef(av, bv)[0, 1])
    return max(0.0, min(1.0, r))


def _depth_correlation(ref_depth: Optional[np.ndarray],
                       render_depth: np.ndarray,
                       mask: np.ndarray) -> Optional[float]:
    if ref_depth is None:
        return None
    if ref_depth.shape != render_depth.shape:
        ref_depth = cv2.resize(ref_depth, (render_depth.shape[1],
                                           render_depth.shape[0]),
                               interpolation=cv2.INTER_NEAREST)
    a = ref_depth.astype(np.float64)
    b = render_depth.astype(np.float64)
    m = mask > 0
    if m.sum() < 10:
        return None
    av, bv = a[m], b[m]
    if av.std() < 1e-9 or bv.std() < 1e-9:
        return None
    return float(np.corrcoef(av, bv)[0, 1])


