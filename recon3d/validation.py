def _crop_warp_matrix(crop_meta: CropMetadata) -> np.ndarray:
    s = crop_meta.scale
    ox, oy = crop_meta.offset
    return np.array([[s, 0.0, -ox * s], [0.0, s, -oy * s]], dtype=np.float64)


def remap_to_crop(img: np.ndarray, crop_meta: CropMetadata,
                  interpolation: int = cv2.INTER_NEAREST) -> np.ndarray:
    m = _crop_warp_matrix(crop_meta)
    w, h = crop_meta.output_size
    return cv2.warpAffine(img, m, (w, h), flags=interpolation,
                          borderMode=cv2.BORDER_CONSTANT, borderValue=0)


