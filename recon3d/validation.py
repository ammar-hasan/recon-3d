def _evidenced(ev, default):
    v = getattr(ev, "value", None)
    if v is None:
        return default
    return v


def _framing(ref_mask: np.ndarray, focal_px: float
             ) -> Tuple[Tuple[float, float, float], float]:
    """Camera translation (x, y, z) + ortho scale so the object fills the
    frame exactly like the reference mask does on the crop canvas."""
    h, w = ref_mask.shape
    ys, xs = np.nonzero(ref_mask)
    if len(xs) == 0:
        return (0.0, 0.0, 2.0), 2.0
    x0, x1 = xs.min(), xs.max() + 1
    y0, y1 = ys.min(), ys.max() + 1
    bw = max(x1 - x0, 1)
    cx = (x0 + x1) / 2.0 / w
    cy = (y0 + y1) / 2.0 / h
    fw = bw / w
    dist = focal_px / bw          # object width 1.0 projects to bw pixels
    view_w = 1.0 / fw             # world width visible at the object plane
    ox = -(cx - 0.5) * view_w
    oy = (cy - 0.5) * view_w      # image y down -> world y up
    return (ox, oy, dist), view_w


def generate_render_script(plan: ConstructionPlan, ref_mask: np.ndarray,
                           project_dir: str, cfg: PipelineConfig) -> str:
    """Write blender/render_validation.py and return its path."""
    cam = plan.camera
    focal_px = cfg.camera.default_focal_px
    projection = "perspective"
    rot = [0.0, 0.0, 0.0]
    orot = [0.0, 0.0, 0.0]
    translation = None
    if cam is not None:
        focal_px = float(_evidenced(cam.focal_length_px, focal_px))
        projection = cam.projection.value
        rot = [float(v) for v in _evidenced(cam.rotation_euler_deg, rot)]
        orot = [float(v) for v in _evidenced(cam.object_rotation_euler_deg, orot)]
        translation = _evidenced(cam.translation, None)
    framed_t, view_w = _framing(ref_mask, focal_px)
    if translation is None:
        translation = list(framed_t)
    else:
        translation = [float(v) for v in translation]
    camera_offset = (plan.metadata or {}).get("validation_camera_offset")
    if camera_offset and len(camera_offset) >= 2:
        translation[0] += float(camera_offset[0])
        translation[1] += float(camera_offset[1])
    params = {
        "render_engine": cfg.blender.render_engine or "BLENDER_EEVEE",
        "resolution": int(ref_mask.shape[0]),
        "output_dir": str(Path(project_dir).resolve() / "validation"),
        "focal_px": focal_px,
        "projection": projection,
        "ortho_scale": view_w,
        "camera_translation": translation,
        "camera_rotation_deg": rot,
        "object_rotation_deg": orot,
        "turntable_frames": 24,
        "turntable_resolution": min(512, int(ref_mask.shape[0])),
        "seed": cfg.seed,
    }
    script = _RENDER_TEMPLATE.replace(
        "__PARAMS_JSON__", json.dumps(json.dumps(params, sort_keys=True)))
    out = Path(project_dir) / "blender"
    out.mkdir(parents=True, exist_ok=True)
    path = out / "render_validation.py"
    path.write_text(script)
    return str(path)


