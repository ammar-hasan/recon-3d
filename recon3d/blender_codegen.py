def _new_mesh_object(name, verts, faces, collection):
    mesh = bpy.data.meshes.new(name + "_mesh")
    mesh.from_pydata(verts, [], faces)
    mesh.validate(clean_customdata=False)
    mesh.update()
    obj = bpy.data.objects.new(name, mesh)
    collection.objects.link(obj)
    return obj


def _axis_basis(direction):
    d = Vector(direction).normalized()
    ref = Vector((0.0, 0.0, 1.0)) if abs(d.z) < 0.9 else Vector((1.0, 0.0, 0.0))
    b1 = d.cross(ref).normalized()
    b2 = d.cross(b1).normalized()
    return d, b1, b2


def build_revolve(name, part, collection):
    profile = (part.get("profile") or {}).get("points") or []
    if len(profile) < 2:
        raise ValueError("revolve part %s needs >=2 profile points" % part.get("id"))
    axis = part.get("axis") or {}
    origin = Vector(axis.get("origin", [0.0, 0.0, 0.0]))
    d, b1, b2 = _axis_basis(axis.get("direction", [0.0, 0.0, 1.0]))
    closed_profile = bool((part.get("profile") or {}).get("closed", False))
    pts = [(float(p[0]), float(p[1])) for p in profile]
    verts = []
    rings = []
    for (r, h) in pts:
        if abs(r) < 1e-9:
            rings.append([len(verts)])
            verts.append(tuple(origin + d * h))
        else:
            ring = []
            for i in range(SEGMENTS):
                a = 2.0 * math.pi * i / SEGMENTS
                p = origin + d * h + (b1 * math.cos(a) + b2 * math.sin(a)) * r
                ring.append(len(verts))
                verts.append(tuple(p))
            rings.append(ring)
    faces = []
    for j in range(len(rings) - 1):
        ra, rb = rings[j], rings[j + 1]
        if len(ra) == 1 and len(rb) == 1:
            continue
        if len(ra) == 1:
            for i in range(SEGMENTS):
                faces.append((ra[0], rb[(i + 1) % SEGMENTS], rb[i]))
        elif len(rb) == 1:
            for i in range(SEGMENTS):
                faces.append((rb[0], ra[i], ra[(i + 1) % SEGMENTS]))
        else:
            for i in range(SEGMENTS):
                i2 = (i + 1) % SEGMENTS
                faces.append((ra[i], rb[i], rb[i2], ra[i2]))
    if closed_profile and len(rings) > 1 and len(rings[0]) == SEGMENTS and len(rings[-1]) == SEGMENTS:
        ra, rb = rings[-1], rings[0]
        for i in range(SEGMENTS):
            i2 = (i + 1) % SEGMENTS
            faces.append((ra[i], rb[i], rb[i2], ra[i2]))
    obj = _new_mesh_object(name, verts, faces, collection)
    for poly in obj.data.polygons:
        poly.use_smooth = True
    return obj


def _extrude_mesh(name, pts2d, depth, collection):
    pts = [(float(p[0]), float(p[1])) for p in pts2d]
    if len(pts) < 3:
        raise ValueError("extrude part %s needs >=3 profile points" % name)
    half = float(depth) / 2.0
    verts = [(x, y, -half) for (x, y) in pts] + [(x, y, half) for (x, y) in pts]
    n = len(pts)
    faces = []
    # caps via tessellation (handles concave polygons)
    tris = tessellate_polygon([[Vector((x, y, 0.0)) for (x, y) in pts]])
    for tri in tris:
        faces.append(tuple(reversed(tuple(tri))))
        faces.append(tuple(n + i for i in tri))
    for i in range(n):
        i2 = (i + 1) % n
        faces.append((i, i2, n + i2, n + i))
    return _new_mesh_object(name, verts, faces, collection)


def build_extrude(name, part, collection):
    profile = part.get("profile") or {}
    pts = profile.get("points") or []
    depth = float(part.get("depth") or 0.05)
    obj = _extrude_mesh(name, pts, depth, collection)
    bevel = obj.modifiers.new("bevel", 'BEVEL')
    bevel.width = min(0.01, depth * 0.1)
    bevel.segments = 2
    bevel.limit_method = 'ANGLE'
    return obj


def build_loft(name, part, collection, subsurf=False):
    profile = part.get("profile") or {}
    sections = profile.get("profiles")
    if not sections:
        # single cross-section lofted between +/- depth/2
        pts = profile.get("points") or []
        depth = float(part.get("depth") or 0.1)
        sections = [{"points": pts, "z": -depth / 2.0},
                    {"points": pts, "z": depth / 2.0}]
    rings = []
    verts = []
    count = max(len(s["points"]) for s in sections)
    for s in sections:
        pts = [(float(p[0]), float(p[1])) for p in s["points"]]
        z = float(s.get("z", 0.0))
        # resample ring to `count` points
        ring = []
        for i in range(count):
            p = pts[int(i * len(pts) / count) % len(pts)]
            ring.append(len(verts))
            verts.append((p[0], p[1], z))
        rings.append(ring)
    faces = []
    for j in range(len(rings) - 1):
        ra, rb = rings[j], rings[j + 1]
        for i in range(count):
            i2 = (i + 1) % count
            faces.append((ra[i], rb[i], rb[i2], ra[i2]))
    for ring, flip in ((rings[0], True), (rings[-1], False)):
        center = len(verts)
        cx = sum(verts[i][0] for i in ring) / count
        cy = sum(verts[i][1] for i in ring) / count
        cz = sum(verts[i][2] for i in ring) / count
        verts.append((cx, cy, cz))
        for i in range(count):
            i2 = (i + 1) % count
            faces.append((center, ring[i2], ring[i]) if flip else (center, ring[i], ring[i2]))
    obj = _new_mesh_object(name, verts, faces, collection)
    for poly in obj.data.polygons:
        poly.use_smooth = True
    if subsurf:
        sub = obj.modifiers.new("subsurf", 'SUBSURF')
        sub.levels = 1
        sub.render_levels = 1
    return obj


def build_sweep(name, part, collection):
    profile = part.get("profile") or {}
    pts = profile.get("points") or []
    if len(pts) < 2:
        raise ValueError("sweep part %s needs >=2 path points" % part.get("id"))
    radius = float(part.get("depth") or 0.02)
    cu = bpy.data.curves.new(name + "_curve", 'CURVE')
    cu.dimensions = '3D'
    cu.resolution_u = 2
    cu.bevel_depth = radius
    cu.bevel_resolution = 3
    cu.use_fill_caps = True
    if len(pts) > 2:
        sp = cu.splines.new('BEZIER')
        sp.bezier_points.add(len(pts) - 1)
        for i, p in enumerate(pts):
            x = float(p[0])
            y = float(p[1])
            z = float(p[2]) if len(p) > 2 else 0.0
            bp = sp.bezier_points[i]
            bp.co = (x, y, z)
            bp.handle_left_type = 'AUTO'
            bp.handle_right_type = 'AUTO'
    else:
        sp = cu.splines.new('POLY')
        sp.points.add(len(pts) - 1)
        for i, p in enumerate(pts):
            x = float(p[0])
            y = float(p[1])
            z = float(p[2]) if len(p) > 2 else 0.0
            sp.points[i].co = (x, y, z, 1.0)
    obj = bpy.data.objects.new(name, cu)
    collection.objects.link(obj)
    return obj


def build_primitive(name, part, collection):
    shape = (part.get("primitive_shape") or "cube").lower()
    scale = (part.get("transform") or {}).get("scale") or [0.2, 0.2, 0.2]
    sx, sy, sz = (float(scale[0]), float(scale[1]), float(scale[2]))
    if shape == "cube":
        bpy.ops.mesh.primitive_cube_add(size=1.0)
        obj = bpy.context.active_object
        obj.scale = (sx, sy, sz)
    elif shape == "cylinder":
        bpy.ops.mesh.primitive_cylinder_add(vertices=SEGMENTS, radius=0.5, depth=1.0)
        obj = bpy.context.active_object
        obj.scale = (sx, sy, sz)
    elif shape == "sphere":
        bpy.ops.mesh.primitive_uv_sphere_add(segments=SEGMENTS, ring_count=24, radius=0.5)
        obj = bpy.context.active_object
        obj.scale = (sx, sy, sz)
        for poly in obj.data.polygons:
            poly.use_smooth = True
    elif shape == "cone":
        bpy.ops.mesh.primitive_cone_add(vertices=SEGMENTS, radius1=0.5, radius2=0.0, depth=1.0)
        obj = bpy.context.active_object
        obj.scale = (sx, sy, sz)
    elif shape == "torus":
        major = sx / 2.0 * 0.75 if sx > 0 else 0.15
        minor = sx / 2.0 * 0.25 if sx > 0 else 0.05
        bpy.ops.mesh.primitive_torus_add(major_radius=major, minor_radius=minor,
                                         major_segments=SEGMENTS, minor_segments=12)
        obj = bpy.context.active_object
        obj.scale = (1.0, sy / sx if sx > 0 else 1.0, 1.0)
        for poly in obj.data.polygons:
            poly.use_smooth = True
    elif shape == "capsule":
        # no capsule op in Blender 5.x: revolve a capsule profile about local Z
        r = sx / 2.0 if sx > 0 else 0.1
        h = max(sz, 2.0 * r + 1e-3)
        prof = []
        steps = 8
        for i in range(steps + 1):
            a = -math.pi / 2.0 + math.pi * i / steps
            prof.append([r * math.cos(a), -h / 2.0 + r + r * math.sin(a)])
        for i in range(steps + 1):
            a = math.pi * i / steps
            prof.append([r * math.cos(a), h / 2.0 - r + r * math.sin(a)])
        fake = {"id": name, "profile": {"points": prof, "closed": False},
                "axis": {"origin": [0, 0, 0], "direction": [0, 0, 1]}}
        obj = build_revolve(name, fake, collection)
    else:
        raise ValueError("unknown primitive_shape %r for part %s" % (shape, part.get("id")))
    # ops-created objects land in the active collection; move to target
    if obj.name not in collection.objects:
        for c in list(obj.users_collection):
            c.objects.unlink(obj)
        collection.objects.link(obj)
    obj.name = name
    if obj.data:
        obj.data.name = name + "_mesh"
    return obj


