"""Stage 15: construction plan -> deterministic Blender Python script.

`generate_blender_script` is a pure function of the plan: for a given
ConstructionPlan it always emits byte-identical script text. The generated
script runs inside Blender background mode and builds a named, hierarchical,
non-destructive scene, then exports scene.blend / model.glb and writes a
blender_manifest.json describing everything it created.

The generated script deliberately avoids: subprocess/shell, network,
eval/exec, ctypes, and any file access outside the paths handed to it via
argv after ``--`` (project_dir, blend path, glb path, manifest path).
"""
