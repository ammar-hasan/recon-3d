_materials = {}


def get_material(spec):
    key = (
        spec.get("material_class", "plastic"),
        tuple(round(float(c), 5) for c in spec.get("base_color", (0.8, 0.8, 0.8))),
        round(float(spec.get("roughness", 0.5)), 4),
        round(float(spec.get("metallic", 0.0)), 4),
        round(float(spec.get("opacity", 1.0)), 4),
        round(float(spec.get("transmission", 0.0)), 4),
    )
    if key in _materials:
        return _materials[key]
    name = "mat_%s_%02d" % (key[0], len(_materials))
    mat = bpy.data.materials.new(name)
    mat.use_nodes = True
    bsdf = mat.node_tree.nodes.get("Principled BSDF")
    r, g, b = key[1]
    bsdf.inputs["Base Color"].default_value = (r, g, b, 1.0)
    bsdf.inputs["Roughness"].default_value = key[2]
    bsdf.inputs["Metallic"].default_value = key[3]
    if "Transmission Weight" in bsdf.inputs:
        bsdf.inputs["Transmission Weight"].default_value = key[5]
    if "Alpha" in bsdf.inputs:
        bsdf.inputs["Alpha"].default_value = key[4]
    if key[4] < 1.0:
        try:
            mat.surface_render_method = 'DITHERED'
        except Exception:
            try:
                mat.blend_method = 'BLEND'
            except Exception:
                pass
    mat.diffuse_color = (r, g, b, key[4])
    _materials[key] = mat
    return mat


