def top_level_ancestor(part_id, by_id):
    pid = part_id
    seen = set()
    while by_id.get(pid) and by_id[pid].get("parent") and pid not in seen:
        seen.add(pid)
        pid = by_id[pid]["parent"]
    return pid


def apply_transform(obj, part):
    tr = part.get("transform") or {}
    loc = tr.get("location")
    if loc:
        obj.location = (float(loc[0]), float(loc[1]), float(loc[2]))
    rot = tr.get("rotation_deg")
    if rot:
        obj.rotation_euler = (math.radians(float(rot[0])),
                              math.radians(float(rot[1])),
                              math.radians(float(rot[2])))
    sc = tr.get("scale")
    if sc and part.get("operator") not in ("primitive", "boolean"):
        # primitive builders already consumed transform.scale
        obj.scale = (float(sc[0]), float(sc[1]), float(sc[2]))


def set_custom_props(obj, part):
    obj["recon3d_part_id"] = part.get("id", "")
    obj["recon3d_operator"] = part.get("operator", "")
    params = {k: part.get(k) for k in (
        "primitive_shape", "axis", "depth", "count", "angle_degrees",
        "source_part", "boolean_target", "boolean_operation", "transform")
        if part.get(k) is not None}
    obj["recon3d_params"] = json.dumps(params, sort_keys=True)
    if part.get("profile") is not None:
        obj["recon3d_profile"] = json.dumps(part["profile"], sort_keys=True)


