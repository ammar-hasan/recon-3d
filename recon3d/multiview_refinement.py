"""Render-based joint multiview pose and depth refinement.

The primary-view construction plan fixes observed XY geometry.  Secondary
silhouettes constrain the otherwise weakly observed Z extent and relative
object yaw.  A single sandboxed Blender invocation renders a bounded grid of
depth scales and yaw hypotheses; the host scores every silhouette, selects one
shared depth scale across views, and retains it only when mean agreement
improves.  This makes Phase 6 alter inferred 3D geometry without overwriting
primary observed curves.
"""
